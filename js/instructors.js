const byslug = new Map(instructors.map((i) => [i.slug, i]));

function renderGrid() {
  const grid = document.getElementById("instructor-grid");
  if (!grid) return;
  grid.innerHTML = instructors
    .map(
      (i) => `
    <button class="instructor-tile" type="button" data-slug="${i.slug}" aria-expanded="false" aria-label="${i.name} — rozbalit detail">
      <img src="img/instructors/${i.slug}.webp"
           srcset="img/instructors/${i.slug}.webp 1x, img/instructors/${i.slug}@2x.webp 2x"
           alt="${i.name}" loading="lazy" width="400" height="400" />
      <span class="label">${i.name}</span>
    </button>
  `,
    )
    .join("");
}

let openSlug = null;
let panel = null;

function makePanel() {
  const p = document.createElement("article");
  p.className = "instructor-detail";
  p.setAttribute("aria-live", "polite");
  p.innerHTML = `
    <div class="instructor-detail__photo-wrap">
      <img class="instructor-detail__photo" alt="" />
    </div>
    <div class="instructor-detail__text">
      <p class="eyebrow" data-field="role"></p>
      <h3 class="instructor-detail__name" data-field="name"></h3>
      <blockquote class="instructor-detail__quote" data-field="quote" hidden></blockquote>
      <p class="instructor-detail__bio" data-field="bio"></p>
    </div>
  `;
  return p;
}

function fillPanel(slug) {
  const i = byslug.get(slug);
  if (!i || !panel) return;
  const photo = panel.querySelector(".instructor-detail__photo");
  photo.src = `img/instructors/${i.slug}@2x.webp`;
  photo.alt = i.name;
  panel.querySelector("[data-field=role]").textContent = i.role || "";
  panel.querySelector("[data-field=name]").textContent = i.name;
  const q = panel.querySelector("[data-field=quote]");
  if (i.quote && i.quote.trim()) {
    q.textContent = `„${i.quote}“`;
    q.hidden = false;
  } else {
    q.hidden = true;
  }
  const b = panel.querySelector("[data-field=bio]");
  b.textContent = i.bio && i.bio.trim() ? i.bio : "Bio bude doplněno.";
}

function findLastInRow(tile) {
  const tiles = [...document.querySelectorAll(".instructor-tile")];
  const top = tile.offsetTop;
  let last = tile;
  for (const t of tiles) {
    if (Math.abs(t.offsetTop - top) < 4 && t.offsetLeft >= last.offsetLeft) {
      last = t;
    }
  }
  return last;
}

function closePanel({ scroll = true } = {}) {
  if (!panel || !panel.parentNode) {
    openSlug = null;
    return;
  }
  const slug = openSlug;
  const tile = slug ? document.querySelector(`[data-slug="${slug}"]`) : null;
  tile?.setAttribute("aria-expanded", "false");
  panel.classList.remove("is-open");
  const onEnd = () => {
    if (!panel.classList.contains("is-open") && panel.parentNode)
      panel.remove();
  };
  panel.addEventListener("transitionend", onEnd, { once: true });
  // Fallback in case transitionend doesn't fire
  setTimeout(onEnd, 500);
  openSlug = null;
  if (scroll && tile) {
    tile.focus({ preventScroll: true });
  }
}

function expandFor(slug) {
  const tile = document.querySelector(`[data-slug="${slug}"]`);
  if (!tile) return;

  if (openSlug === slug) {
    closePanel();
    return;
  }

  // Reset previously expanded tile
  if (openSlug) {
    document
      .querySelector(`[data-slug="${openSlug}"]`)
      ?.setAttribute("aria-expanded", "false");
  }

  if (!panel) panel = makePanel();

  // Detach panel first so row layout is clean before measuring
  if (panel.parentNode) panel.remove();

  const lastInRow = findLastInRow(tile);
  lastInRow.after(panel);

  fillPanel(slug);
  openSlug = slug;
  tile.setAttribute("aria-expanded", "true");

  requestAnimationFrame(() => {
    requestAnimationFrame(() => panel.classList.add("is-open"));
  });

  // Bring the tile + opened panel into a comfortable view
  setTimeout(() => {
    panel.scrollIntoView({ behavior: "smooth", block: "nearest" });
  }, 120);
}

function wireDetail() {
  const grid = document.getElementById("instructor-grid");
  if (!grid) return;

  document.addEventListener("click", (e) => {
    const tile = e.target.closest(".instructor-tile");
    if (tile && grid.contains(tile)) {
      expandFor(tile.dataset.slug);
      return;
    }
    if (openSlug && panel && !panel.contains(e.target)) {
      closePanel({ scroll: false });
    }
  });

  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && openSlug) closePanel();
  });

  let resizeT;
  window.addEventListener("resize", () => {
    if (!openSlug) return;
    clearTimeout(resizeT);
    resizeT = setTimeout(() => closePanel({ scroll: false }), 150);
  });
}

function wireReveal() {
  const els = document.querySelectorAll(".reveal");
  if (!("IntersectionObserver" in window) || els.length === 0) {
    els.forEach((el) => el.classList.add("is-visible"));
    return;
  }
  const io = new IntersectionObserver(
    (entries) => {
      for (const en of entries) {
        if (en.isIntersecting) {
          en.target.classList.add("is-visible");
          io.unobserve(en.target);
        }
      }
    },
    { threshold: 0.12 },
  );
  els.forEach((el) => io.observe(el));
}

document.addEventListener("DOMContentLoaded", () => {
  renderGrid();
  wireDetail();
  wireReveal();
});
